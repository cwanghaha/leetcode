uint32_t reverseBits(uint32_t n)
{
    if((0 == n)||(0xffffffff == n))
    {
        return n;
    }

    uint32_t ret = 0;

    for(int i=0;i<32;i++)
    {
        ret<<=1;
        ret |= n&0x1;
        n>>=1;
    }

    return ret;
}